public class TiposPrimitivos {
    public static void main(String[] args) {
        // Tipo Primitivo
        int a1 = 10;
        // Tipo Não Prmitivo
        Integer a2 = 10;

        /**
         * A prinicpal diferença é que quando declarado
         * Tipos Primitivos NÃO possuem metodos, já os
         * Tipos Não Primitivos, são Orientados a Objetos,
         * sendo possível relacionar metodos a eles.
         **/

        /*
        *               Tipos
        * Não Primitivos        Primitivos
        *   Double                double
        *   Float                 float
        *   Long                  long
        *   Integer               int
        *   Short                 short
        *   Byte                  byte
        *   Boolean               boolean
        *   Character             char
        * */

        /**
         * String é o único Tipo que NÃO tem Tipo Primitivo,
         * devido ser mais complexo, por ser uma sequência
         * de caracteres.
         */


    }
}
